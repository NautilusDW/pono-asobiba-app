// Step C クラウド同期の「合言葉」用 kid-safe ひらがな word list。
//
// ルール (CRITICAL-1 / 仕様書):
//   - 清音のみ (seion)。 濁点 (゛) / 半濁点 (゜) を含む文字は一切使わない。
//     → 保護者が電話/メモで書き写すときの聞き間違い (ば/ぱ, が/か など) を排除。
//   - 「を」「ゐ」「ゑ」「ー」は使わない (お/長音との混同回避)。
//   - 実在する平易な語のみ (子供・保護者が読める)。 小書き (っ ゃ ゅ ょ) は許容。
//
// エントロピー設計:
//   合言葉 = ひらがな 3 語 (UNION から独立抽出、 重複可) + 4 桁数字。
//   各スロットは 3 セットを結合した UNION 全体から選ぶ (per-set 選択より約 +4.75bit 有利)。
//   実効エントロピー = 3*log2(WORDS.length) + 4*log2(10)。
//   41bit 床を確実に超えるには UNION >= 604 が必要 (= 3*log2(604)+4*log2(10) ≈ 41.0bit)。
//   本 list は UNION >= 604 を満たすよう構成 (実測は passcode.js / tests / verify script)。
//
//   ※ 仕様書の「150^3 × 10^4 ≈ 47bit」は算術誤り (per-set 選択なら実際は約 35bit)。
//     本実装は UNION 選択 + 語数拡張で 41bit 以上を担保。
//
// 語を追加/変更するとき:
//   1) 清音のみか (濁点/半濁点/を/ー を含まないか) を必ず検査
//      → scripts と同等の再現手順は tests/savedata-api.test.mjs / verify script 参照
//   2) UNION は Set で自動 dedup されるので重複は自然に無視される
//   3) UNION が 604 を下回るとエントロピーが 41bit を割るので、削除時は要注意
// -----------------------------------------------------------------------------

